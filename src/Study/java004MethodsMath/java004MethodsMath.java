package Study.java004MethodsMath;

public class java004MethodsMath {

    public static void main(String[] args){

        double x = 3.14159;
        double y = -10.0;
        double z = 0;

        z = Math.max(x, y);
        System.out.println(z);
        z = Math.round(x);
        System.out.println(z);
        z = Math.min(x, y);
        System.out.println(z);
        z = Math.abs(y);
        System.out.println(z);
        z = Math.sqrt(z);
        System.out.println(z);
    }

}